package nws.models.point;

/**
 * The properties for a {@link Point}
 */
public class PointProperties {
    /**
     * The county warning area for a {@link Point}
     */
    public String cwa;
    /**
     * The forecast office link for a {@link Point}
     */
    public String forecastOffice;
    /**
     * The grid's ID for a {@link Point}
     */
    public String gridId;
    /**
     * The grid's X coordinate for a {@link Point}
     */
    public String gridX;
    /**
     * The grid's Y coordinate for a {@link Point}
     */
    public String gridY;
    /**
     * The forecast link for a {@link Point}
     */
    public String forecast;
    /**
     * The hourly forecast link for a {@link Point}
     */
    public String forecastHourly;
    /**
     * The forecast grid data link for a {@link Point}
     */
    public String forecastGridData;
    /**
     * The observation stations link for a {@link Point}
     */
    public String observationStations;
    /**
     * A relative location for a {@link Point}
     */
    public RelativeLocation relativeLocation;
    /**
     * The forecast zone for a {@link Point}
     */
    public String forecastZone;
    /**
     * The county for a {@link Point}
     */
    public String county;
    /**
     * The fire weather zone for a {@link Point}
     */
    public String fireWeatherZone;
    /**
     * The time zone for a {@link Point}
     */
    public String timeZone;
    /**
     * The radar station for a {@link Point}
     */
    public String radarStation;
}
